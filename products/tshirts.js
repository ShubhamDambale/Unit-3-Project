 var cartData = JSON.parse(localStorage.getItem("cart")) || [];





 DisplayData(tshirts);
  
 function DisplayData(tshirts){
   document.querySelector("#container").innerHTML= "";


 

tshirts.map(function(elem){

    var box = document.createElement("div");

    var img = document.createElement("img");
    img.setAttribute("class", "img_div")
    img.src = elem.img_div;

    console.log(img)

    var names = document.createElement("p");
    names.setAttribute("class", "names_div")
    names.textContent = elem.brandname;

    var prices = document.createElement("p");
    prices.setAttribute("class", "prices_div")
    prices.innerText = elem.title;

    

     var news = document.createElement("p");
     news.setAttribute("class", "news_div");
     news.innerText = elem.price;

    

     var btn = document.createElement("button");
     btn.setAttribute("class", "buttons_div");
     btn.innerText = "Add To Cart";
     btn.addEventListener("click", function() {
        addToCart(elem);
     })

     
    

    box.append(img,names,prices,news,btn);

    document.querySelector("#container").append(box);
})

function addToCart(elem){
    console.log(elem);
    cartData.push(elem);
    localStorage.setItem("cart", JSON.stringify(cartData));
}

}



// Sorting
function sortItems(){
    var sorting = document.getElementById("sorter").value;
       
    console.log(sorting);

    if (sorting=="h2l"){
        shirts.sort(function(a, b){
            return b.price - a.price
        });

        DisplayData(tshirts);
    }
    else  if (sorting=="l2h"){
        tshirts.sort(function(a, b){
            return a.price - b.price
        });

        DisplayData(tshirts);
    }
  }